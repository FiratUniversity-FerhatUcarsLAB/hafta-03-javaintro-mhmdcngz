public class MilKilometreTablosu{
	public static void main(String[] args){
		double m1 = 1;
		double m2 = 5;
		double m3 = 10;
		double m4 = 20;
		double m5 = 50;
		double a = 1.609;
		System.out.printf("1 mil = %f km", m1*a);
		System.out.printf("\n5 mil = %f km", m2*a);
		System.out.printf("\n10 mil = %f km", m3*a);
		System.out.printf("\n20 mil = %f km", m4*a);
		System.out.printf("\n50 mil = %f km", m5*a);
	}
}
	